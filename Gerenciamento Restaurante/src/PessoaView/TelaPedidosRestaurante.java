    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        int numero = Integer.parseInt(txtNu.getText().trim());
        String cliente = txtNome.getText().trim();
        String itens = txtItens.getText().trim();
        double total = Double.parseDouble(txtTotal.getText().trim());

        if (cliente.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, digite o nome do cliente.");
            return;
        }
            RestauranteDTO pedido = new RestauranteDTO(numero, cliente, itens, total);
             RestauranteDAO.adicionarPedido(pedido);

            JOptionPane.showMessageDialog(null,
                    "Pedido registrado com sucesso para o cliente: " + cliente + "!");
           String lista = RestauranteDAO.listarPedidos();
                JOptionPane.showMessageDialog(null, lista);

    }//GEN-LAST:event_btnListarActionPerformed
    
