    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        String cliente = txtNome.getName();
        int numero = Integer.parseInt(TxtQuarto.getText());
        String entrada = txtEnt.getText();
        String saida = txtSai.getText();
       
        HotelDTO reserva = new HotelDTO(cliente, numero, entrada, saida);
        HotelDAO.adicionarReserva(reserva);
        String lista = HotelDAO.listarReservas();
        JOptionPane.showMessageDialog(null, lista);
        JOptionPane.showMessageDialog(null,
                "Reserva feita com sucesso para " + cliente + "!");
    }//GEN-LAST:event_btnListarActionPerformed

